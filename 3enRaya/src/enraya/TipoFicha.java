package enraya;

/**
 * Define el tipo de ficha del <b>3 en Raya</b>.
 * @author FutuzorMekkla
 */

public enum TipoFicha {
    /**
     * La ficha es un círculo: <code>O</code>
     */
    O,
    /**
     * La ficha es una équis: <code>X</code>
     */
    X,
    /**
     * La ficha está vacía, por lo que no se muestra en pantalla.
     */
    VAC;
}
